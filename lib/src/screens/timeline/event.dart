class Event {
  int? id;
  String eventTitle = "";
  String location = "LOCATION";
  String date = "";
  DateTime realDate;

  Event(this.id, this.eventTitle, this.location, this.date, this.realDate) {
    id = id;
    eventTitle = eventTitle;
    location = location;
    date = date;
    realDate = realDate;
  }
}